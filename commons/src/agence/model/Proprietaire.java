package agence.model;

public class Proprietaire {
	private int idProp;
	private String nom;
	private String prenom;
	private String adress;
	private String numPriv;
	private String numTrav;
	private String debutDispo;
	private String finDispo;
	public int getIdProp() {
		return idProp;
	}
	public void setIdProp(int idProp) {
		this.idProp = idProp;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getPrenom() {
		return prenom;
	}
	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
	public String getAdress() {
		return adress;
	}
	public void setAdress(String adress) {
		this.adress = adress;
	}
	public String getNumPriv() {
		return numPriv;
	}
	public void setNumPriv(String numPriv) {
		this.numPriv = numPriv;
	}
	public String getNumTrav() {
		return numTrav;
	}
	public void setNumTrav(String numTrav) {
		this.numTrav = numTrav;
	}
	public String getDebutDispo() {
		return debutDispo;
	}
	public void setDebutDispo(String debutDispo) {
		this.debutDispo = debutDispo;
	}
	public String getFinDispo() {
		return finDispo;
	}
	public void setFinDispo(String finDispo) {
		this.finDispo = finDispo;
	}
	
	
	

}
